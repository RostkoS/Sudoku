public class WrongInput {// for saving inputted wrong values
    //position
    private int i;
    private int j;
    //value
    private int num;

    //constructor
    public WrongInput(int i, int j, int num) {
        this.i = i;
        this.j = j;
        this.num = num;
    }

    public int getI() {
        return i;
    }
    public int getJ() {
        return j;
    }
    public int getNum() {
        return num;
    }

}
